func Gen(parserFile string, g *ast.GoGLL, gs *gslot.GSlot, ff *frstflw.FF, ts *tokens.Tokens) {
	if err := ioutil.WriteFile(parserFile, []byte(tmplSrc)); err != nil {
		panic(err)
	}
}

const tmplSrc = `//! Module parser is generated by GoGLL. Do not edit.

mod bsr;
mod slot;
mod symbols;
`
